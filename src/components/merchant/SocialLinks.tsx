const SOCIAL_ICON_MAP: Record<string, React.ComponentType<{ className?: string }>> = {
  instagram: InstagramIcon,
  facebook: FacebookIcon,
  tiktok: TikTokIcon,
  youtube: YoutubeIcon,
  twitter: TwitterIcon,
  x: TwitterIcon,
  whatsapp: WhatsAppIcon,
  zalo: ZaloIcon,
  telegram: TelegramIcon,
};

const SOCIAL_COLOR_MAP: Record<string, string> = {
  instagram: "text-pink-600 hover:text-pink-700",
  facebook: "text-blue-600 hover:text-blue-700",
  tiktok: "text-gray-900 hover:text-gray-700",
  youtube: "text-red-600 hover:text-red-700",
  twitter: "text-gray-900 hover:text-gray-700",
  x: "text-gray-900 hover:text-gray-700",
  whatsapp: "text-green-600 hover:text-green-700",
  zalo: "text-blue-500 hover:text-blue-600",
  telegram: "text-blue-400 hover:text-blue-500",
};

export function SocialLinks({ socialLinks, youtubeUrl }: SocialLinksProps) {
  // Combine social links with YouTube if it exists
  const allLinks = [...socialLinks];
  if (youtubeUrl && !socialLinks.some(link => link.type.toLowerCase() === 'youtube')) {
    allLinks.push({
      id: 'youtube',
      type: 'youtube',
      url: youtubeUrl,
    });
  }

  if (allLinks.length === 0) return null;

  return (
    <div className="bg-white rounded-2xl p-6 shadow-sm">
      <h3 className="font-bold text-gray-900 mb-4">Theo dõi chúng tôi</h3>
      <div className="flex flex-wrap gap-3">
        {allLinks
          .filter((link) => SOCIAL_ICON_MAP[link.type.toLowerCase()])
          .map((link, index) => {
            const Icon = SOCIAL_ICON_MAP[link.type.toLowerCase()];
            const colorClass = SOCIAL_COLOR_MAP[link.type.toLowerCase()] || "text-gray-600 hover:text-gray-700";

            return (
              <a
                key={link.id || `${link.type}-${index}`}
                href={link.url}
                target="_blank"
                rel="noopener noreferrer"
                className={`p-3 rounded-xl border border-gray-200 hover:border-gray-300 transition-all ${colorClass} hover:shadow-md`}
                title={link.label || link.type}
              >
                <Icon className="h-5 w-5" />
              </a>
            );
          })}
      </div>    </div>
  );
}
